+ (void)saveRecent:(NSString *)enjoyString {
    NSString *root = [NSSearchPathForDirectoriesInDomains(NSDocumentDirectory, NSUserDomainMask, YES) firstObject];
    NSString * user = [YTLoginUserInfo shareUserInfo].uid;
    root = [root stringByAppendingPathComponent:user];
    NSString *filePath = root;
    filePath = [filePath stringByAppendingPathComponent:@"fileCach"];
    filePath = [filePath stringByAppendingPathComponent:@"recentFace.plist"];
    
    NSMutableArray * muArray = [[NSMutableArray alloc] initWithContentsOfFile:filePath];
    if (!muArray) {
        muArray = [NSMutableArray array];
    }
    if (muArray.count >= 20) {
        [muArray removeObjectsInRange:NSMakeRange(19, muArray.count - 19)];
    }
    for (NSString * tmp in muArray) {
        if ([tmp isEqualToString:enjoyString]) {
            [muArray insertObject:enjoyString atIndex:0];
            [muArray removeObject:tmp];
            break;
        }
    }
    [muArray insertObject:enjoyString atIndex:0];
    BOOL is =  [muArray writeToFile:filePath atomically:YES];
}

