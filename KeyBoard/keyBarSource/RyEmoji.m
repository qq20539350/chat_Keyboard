+ (NSString *)emojiWithCode:(int)code {
    char chars[4];
    int len = 4;
    
    chars[0] = (code >> 24) & (1 << 24) - 1;
    chars[1] = (code >> 16) & (1 << 16) - 1;
    chars[2] = (code >> 8) & (1 << 8) - 1;
    chars[3] = code & (1 << 8) - 1;
    
    return [[NSString alloc] initWithBytes:chars
                                    length:len
                                  encoding:NSUTF32StringEncoding];
}

